    def get_TPT(self):
        return self.TPT

    def get_FT(self):
        return self.FT

    def get_REB(self):
        return self.REB

    def get_AST(self):
        return self.AST

    def get_STL(self):
        return self.STL

    def get_BLK(self):
        return self.BLK

    def get_TO(self):
        return self.TO

    def get_PF(self):
        return self.PF